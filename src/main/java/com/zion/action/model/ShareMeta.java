package com.zion.action.model;

public class ShareMeta {

    private String title;

    private String description;

    private String url;

    private String type;

    private String imageUrl;

    public ShareMeta() {

    }

    public ShareMeta(String title, String description, String url, String imageUrl) {
        this(title, description, url, "website", imageUrl);
    }

    public ShareMeta(String title, String description, String url, String type, String imageUrl) {
        this.title = title;
        this.description = description;
        this.url = url;
        this.type = type;
        this.imageUrl = imageUrl;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public String getUrl() {
        return url;
    }

    public String getType() {
        return type;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

}
